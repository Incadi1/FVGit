using Mirror;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.Networking;



//NetworkBehaviour
public class PlayerMovements : MonoBehaviour
{
    public float speedMovement = 2f;
    public float speedRotation = 200.0f;
    private Animator anim;
    public float x, y;

    public bool t = true;

    //Camera
    public GameObject playerCamera;

    public object NetworkManager { get; private set; }

    //Start is called before the first frame update
   void Start()
    {
        anim = GetComponent<Animator>();

    }
   

    // Update is called once per frame
    void Update()
    {
        x = Input.GetAxis("Vertical");
        y = Input.GetAxis("Horizontal");

        //Linea de comandos
        transform.Rotate(0, x * Time.deltaTime * speedRotation, 0);//y
        transform.Translate(0, 0, y * Time.deltaTime * speedMovement);//x

        anim.SetFloat("speedX", x);
        anim.SetFloat("speedY", y);

        anim.SetFloat("speedX", Input.GetAxis("Vertical"));
        anim.SetFloat("speedY", Input.GetAxis("Horizontal"));

    }
}
